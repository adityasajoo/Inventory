    void materialNameAndMax(){
        try{
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:inventory.sql");
            System.out.println("Connected to db");
            String sql = "select material,quantity,unit from items where codeno=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, codeField.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                materialName.setText(rs.getString("material"));
                maxQuantity.setText("MAX : "+rs.getString("quantity"));
                unitField.setText(rs.getString("unit"));
                
            }else{
                materialName.setText("Not Found");
                maxQuantity.setText("");
            }
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "SQL Error");
        }
    }

