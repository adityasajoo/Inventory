    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try{
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:inventory.sql");
            System.out.println("Connected to db");
            String sql = "select * from items where codeno=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, codeField.getText());
            ResultSet rs = pst.executeQuery();
            Double qty = rs.getDouble("quantity");
            String unit = rs.getString("unit");
       
            if(Double.parseDouble(quantityField.getText()) > qty){
                JOptionPane.showMessageDialog(null, "Not enough items in the inventory");
                quantityField.setText("");
                
            }else{
                //Check unit
                if(rs.getString("unit").equals(unitField.getText())){
                    //unit matched
                    sql = "update items set quantity = quantity - ?"
                            +"where codeno = ?";
                    pst = con.prepareStatement(sql);
                    pst.setDouble(1,Double.parseDouble(quantityField.getText()));
                    pst.setString(2, codeField.getText());
                    pst.executeUpdate();
                   
                    JOptionPane.showMessageDialog(null, "Successfull !");
                }else{
                    //Not matched
                    JOptionPane.showMessageDialog(null, "Enter a valid unit");
                    unitField.setText(unit);
                }
            }
            
            
            con.close();
             
        }catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "SQL Error");
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

